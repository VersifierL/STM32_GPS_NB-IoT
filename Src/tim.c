void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM2)
	{
		time_tick++;
		if(time_tick % 1 == 0)
		{
			Time_Tick.time_1s = 1;
		}
		if(time_tick % 15 == 0)
		{
			Time_Tick.time_15s = 1;
		}
		if(time_tick % 20 == 0)
		{
			Time_Tick.time_20s = 1;
		}
		
		if(time_tick == 60000)
			time_tick = 0;
	}
}

